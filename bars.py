"""
Bars Moudle
============
这是一个打印不同分割线的实例模块
"""

def starbar(num):
    """
    打印 * 分割线
    :param num: 线长
    :return: 
    """
    print('*' * num)

def hashbar(num):
    """
    打印 # 分割线
    :param num: 线长
    :return: 
    """
    print('#' * num)

def simplebar (num):
    """
    打印 - 分割线
    :param num: 线长
    :return: 
    """
    print('-' * num)

